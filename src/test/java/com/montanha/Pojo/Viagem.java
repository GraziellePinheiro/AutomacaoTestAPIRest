package com.montanha.Pojo;

public class Viagem {
    public String localDeDestino;
    public String dataPartida;
    public String dataRetorno;
    public String acompanhante;
    public String regiao;



    public String getLocalDeDestino() {
        return localDeDestino;
    }
    public void setLocalDeDestino(String localDeDestino) {
        this.localDeDestino = localDeDestino;
    }

    public String getDataPartida() {
        return dataPartida;
    }
    public void setDataPartida(String dataPartida) {
        this.dataPartida = dataPartida;
    }

    public String getDataRetorno() {
        return dataRetorno;
    }
    public void setDataRetorno(String dataRetorno) {
        this.dataRetorno = dataRetorno;
    }

    public String getAcompanhante() {
        return acompanhante;
    }
    public void setAcompanhante(String acompanhante) {
        this.acompanhante = acompanhante;
    }
    
    public String getRegiao() {
        return regiao;
    }
    public void setRegiao(String regiao) {
        this.regiao = regiao;
    }




}
